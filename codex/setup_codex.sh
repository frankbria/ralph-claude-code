#!/bin/bash

# Ralph Project Setup Script for Codex CLI
# Creates project structure with Ralph-specific files in .ralph/ subfolder
# Parallel to setup.sh (Claude Code) — identical project structure, Codex-specific .ralphrc
#
# Version: 0.1.0

set -e

PROJECT_NAME=${1:-"my-project"}

echo "🚀 Setting up Ralph+Codex project: $PROJECT_NAME"

# Create project directory
mkdir -p "$PROJECT_NAME"
cd "$PROJECT_NAME"

# Determine templates directory location
TEMPLATES_DIR=""
LIB_DIR=""
if [[ -d "../templates" ]]; then
    TEMPLATES_DIR="../templates"
    LIB_DIR="../lib"
elif [[ -d "$HOME/.ralph/templates" ]]; then
    TEMPLATES_DIR="$HOME/.ralph/templates"
    LIB_DIR="$HOME/.ralph/lib"
else
    echo "❌ Error: Templates directory not found."
    echo "   Expected at: ../templates or ~/.ralph/templates"
    echo "   Please run ./install.sh first to install Ralph globally."
    exit 1
fi

# Verify required template files exist
if [[ ! -f "$TEMPLATES_DIR/PROMPT.md" ]]; then
    echo "❌ Error: Required template file PROMPT.md not found in $TEMPLATES_DIR"
    exit 1
fi

# Create structure
mkdir -p src
mkdir -p .ralph/{specs/stdlib,examples,logs,docs/generated}

# Copy templates to .ralph/
cp "$TEMPLATES_DIR/PROMPT.md" .ralph/
cp "$TEMPLATES_DIR/fix_plan.md" .ralph/fix_plan.md
cp "$TEMPLATES_DIR/AGENT.md" .ralph/AGENT.md
cp -r "$TEMPLATES_DIR/specs"/* .ralph/specs/ 2>/dev/null || true

# Generate Codex-specific .ralphrc.codex configuration (separate from Claude's .ralphrc)
cat > .ralphrc.codex << RALPHRCEOF
# .ralphrc.codex - Ralph project configuration (Codex CLI)
# Generated by: ralph-codex-setup
# Documentation: https://github.com/frankbria/ralph-claude-code

# Project identification
PROJECT_NAME="${PROJECT_NAME}"
PROJECT_TYPE="generic"

# Engine selection
RALPH_ENGINE="codex"

# Loop settings
MAX_CALLS_PER_HOUR=100
CODEX_TIMEOUT_MINUTES=30

# Codex-specific settings
# CODEX_MAX_ACU=             # Maximum ACU limit per session (empty = no limit)
# CODEX_API_TOKEN=           # Set via 'codex configure' or environment variable

# Session management
CODEX_USE_CONTINUE=true
CODEX_SESSION_EXPIRY_HOURS=24

# Polling configuration
CODEX_POLL_INTERVAL=15       # Seconds between status polls
CODEX_MAX_POLL_ATTEMPTS=240  # Max polls before timeout (240 * 15s = 60min)

# Task sources (for ralph-codex-enable --sync)
# Options: local, beads, github (comma-separated for multiple)
TASK_SOURCES="local"
GITHUB_TASK_LABEL="ralph-task"
BEADS_FILTER="status:open"

# Circuit breaker thresholds
CB_NO_PROGRESS_THRESHOLD=3
CB_SAME_ERROR_THRESHOLD=5
CB_OUTPUT_DECLINE_THRESHOLD=70

# PR integration (ralph auto-raises PRs at end of each loop)
PR_ENABLED=true
PR_BASE_BRANCH=main
PR_DRAFT=false
MAX_QG_RETRIES=3
RALPHRCEOF

# Initialize git (skip if already initialized)
if [[ ! -d ".git" ]]; then
    git init
fi
echo "# $PROJECT_NAME" > README.md
git add .
git commit -m "Initial Ralph+Codex project setup"

echo "✅ Project $PROJECT_NAME created with Codex CLI support!"
echo ""
echo "Next steps:"
echo "  1. Edit .ralph/PROMPT.md with your project requirements"
echo "  2. Update .ralph/specs/ with your project specifications"
echo "  3. Run: ralph-codex --monitor"
echo "  4. Monitor: ralph-codex-monitor"

#!/bin/bash

# Ralph Enable for Codex CLI - Interactive Wizard for Existing Projects
# Adds Ralph+Codex configuration to an existing codebase
# Parallel to ralph_enable.sh (Claude Code)
#
# Usage:
#   ralph-codex-enable              # Interactive wizard
#   ralph-codex-enable --from beads # With specific task source
#   ralph-codex-enable --force      # Overwrite existing .ralph/
#   ralph-codex-enable --skip-tasks # Skip task import
#
# Version: 0.1.0

set -e

# Get script directory for library loading
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
RALPH_ROOT="$(cd "$SCRIPT_DIR/.." && pwd)"

# Try to load libraries from global installation first, then local
RALPH_HOME="${RALPH_HOME:-$HOME/.ralph}"
if [[ -f "$RALPH_HOME/lib/enable_core.sh" ]]; then
    LIB_DIR="$RALPH_HOME/lib"
elif [[ -f "$RALPH_ROOT/lib/enable_core.sh" ]]; then
    LIB_DIR="$RALPH_ROOT/lib"
else
    echo "Error: Cannot find Ralph libraries"
    echo "Please run ./install.sh first or ensure RALPH_HOME is set correctly"
    exit 1
fi

# Source shared libraries
source "$LIB_DIR/enable_core.sh"
source "$LIB_DIR/wizard_utils.sh"
source "$LIB_DIR/task_sources.sh"

# Source Codex adapter
if [[ -f "$SCRIPT_DIR/lib/codex_adapter.sh" ]]; then
    RALPH_DIR=".ralph"
    source "$SCRIPT_DIR/lib/codex_adapter.sh"
elif [[ -f "$RALPH_HOME/codex/lib/codex_adapter.sh" ]]; then
    RALPH_DIR=".ralph"
    source "$RALPH_HOME/codex/lib/codex_adapter.sh"
fi

# =============================================================================
# CONFIGURATION
# =============================================================================

FORCE_OVERWRITE=false
SKIP_TASKS=false
TASK_SOURCE=""
PRD_FILE=""
GITHUB_LABEL=""
NON_INTERACTIVE=false
SHOW_HELP=false
VERSION="0.1.0"

# =============================================================================
# HELP
# =============================================================================

show_help() {
    cat << EOF
Ralph Enable for Codex CLI - Add Ralph+Codex to Existing Projects

Usage: ralph-codex-enable [OPTIONS]

Options:
    --from <source>     Import tasks from: beads, github, prd
    --prd <file>        PRD file to convert (when --from prd)
    --label <label>     GitHub label filter (when --from github)
    --force             Overwrite existing .ralph/ configuration
    --skip-tasks        Skip task import, use default templates
    --non-interactive   Run with defaults (no prompts)
    -h, --help          Show this help message
    -v, --version       Show version

Examples:
    cd my-existing-project
    ralph-codex-enable

    ralph-codex-enable --from beads
    ralph-codex-enable --from github --label "ralph-task"
    ralph-codex-enable --from prd --prd ./docs/requirements.md
    ralph-codex-enable --skip-tasks
    ralph-codex-enable --force

What this command does:
    1. Detects your project type (TypeScript, Python, etc.)
    2. Identifies available task sources (beads, GitHub, PRDs)
    3. Imports tasks from selected sources
    4. Creates .ralph/ configuration directory
    5. Generates PROMPT.md, fix_plan.md, AGENT.md
    6. Creates .ralphrc.codex with Codex-specific settings (separate from Claude's .ralphrc)

This command is the Codex CLI equivalent of 'ralph-enable'.

EOF
}

# =============================================================================
# CODEX-SPECIFIC RALPHRC GENERATION
# =============================================================================

generate_codex_ralphrc() {
    local project_name="${1:-$(basename "$(pwd)")}"
    local project_type="${2:-generic}"
    local task_sources="${3:-local}"

    cat << RALPHRCEOF
# .ralphrc.codex - Ralph project configuration (Codex CLI)
# Generated by: ralph-codex-enable
# Documentation: https://github.com/frankbria/ralph-claude-code

# Project identification
PROJECT_NAME="${project_name}"
PROJECT_TYPE="${project_type}"

# Engine selection
RALPH_ENGINE="codex"

# Loop settings
MAX_CALLS_PER_HOUR=100
CODEX_TIMEOUT_MINUTES=30

# Codex-specific settings
# CODEX_MAX_ACU=             # Maximum ACU limit per session
# CODEX_API_TOKEN=           # Set via 'codex configure' or environment variable

# Session management
CODEX_USE_CONTINUE=true
CODEX_SESSION_EXPIRY_HOURS=24

# Polling configuration
CODEX_POLL_INTERVAL=15
CODEX_MAX_POLL_ATTEMPTS=240

# Task sources
TASK_SOURCES="${task_sources}"
GITHUB_TASK_LABEL="ralph-task"
BEADS_FILTER="status:open"

# Circuit breaker thresholds
CB_NO_PROGRESS_THRESHOLD=3
CB_SAME_ERROR_THRESHOLD=5
CB_OUTPUT_DECLINE_THRESHOLD=70
RALPHRCEOF
}

# =============================================================================
# WIZARD PHASES
# =============================================================================

run_wizard() {
    echo ""
    echo "╔════════════════════════════════════════════════╗"
    echo "║   Ralph Enable for Codex CLI - Setup Wizard   ║"
    echo "║   Version: $VERSION                            ║"
    echo "╚════════════════════════════════════════════════╝"
    echo ""

    # Check for existing .ralph/ directory
    if [[ -d ".ralph" && "$FORCE_OVERWRITE" != "true" ]]; then
        echo "⚠️  A .ralph/ directory already exists in this project."
        echo ""
        if [[ "$NON_INTERACTIVE" == "true" ]]; then
            echo "Use --force to overwrite existing configuration."
            exit 1
        fi
        read -p "Overwrite existing configuration? [y/N] " -n 1 -r
        echo ""
        if [[ ! $REPLY =~ ^[Yy]$ ]]; then
            echo "Aborted. Use --force to overwrite."
            exit 0
        fi
    fi

    # Phase 1: Environment Detection
    echo "━━━ Phase 1/5: Environment Detection ━━━"
    local project_type="generic"

    if [[ -f "package.json" ]]; then
        project_type="typescript"
        if grep -q '"next"' package.json 2>/dev/null; then
            echo "  Detected: Next.js (TypeScript)"
        elif grep -q '"react"' package.json 2>/dev/null; then
            echo "  Detected: React (TypeScript)"
        else
            echo "  Detected: Node.js/TypeScript"
        fi
    elif [[ -f "requirements.txt" || -f "pyproject.toml" || -f "setup.py" ]]; then
        project_type="python"
        if [[ -f "manage.py" ]]; then
            echo "  Detected: Django (Python)"
        elif grep -q "fastapi" requirements.txt 2>/dev/null; then
            echo "  Detected: FastAPI (Python)"
        else
            echo "  Detected: Python"
        fi
    elif [[ -f "Cargo.toml" ]]; then
        project_type="rust"
        echo "  Detected: Rust"
    elif [[ -f "go.mod" ]]; then
        project_type="go"
        echo "  Detected: Go"
    else
        echo "  Detected: Generic project"
    fi

    # Check Codex CLI
    echo ""
    echo "  Checking Codex CLI..."
    if command -v codex &>/dev/null; then
        echo "  ✅ Codex CLI found"
    else
        echo "  ⚠️  Codex CLI not found. Install: pip install codex-cli"
        echo "  The project will still be configured, but you'll need codex-cli to run ralph-codex"
    fi

    echo ""

    # Phase 2: Task Source Selection
    echo "━━━ Phase 2/5: Task Source Selection ━━━"
    local task_source_type="local"

    if [[ -n "$TASK_SOURCE" ]]; then
        task_source_type="$TASK_SOURCE"
        echo "  Using specified source: $task_source_type"
    elif [[ "$SKIP_TASKS" == "true" ]]; then
        echo "  Skipping task import (using defaults)"
    elif [[ "$NON_INTERACTIVE" == "true" ]]; then
        echo "  Using default task source: local"
    else
        echo "  Available task sources:"
        echo "    1) Local (default templates)"
        echo "    2) Beads (bd list)"
        echo "    3) GitHub Issues"
        echo "    4) PRD Document"
        echo ""
        read -p "  Select source [1-4, default 1]: " -n 1 -r source_choice
        echo ""
        case "$source_choice" in
            2) task_source_type="beads" ;;
            3) task_source_type="github" ;;
            4) task_source_type="prd" ;;
            *) task_source_type="local" ;;
        esac
    fi

    echo ""

    # Phase 3: Configuration
    echo "━━━ Phase 3/5: Configuration ━━━"
    local project_name
    project_name=$(basename "$(pwd)")
    echo "  Project name: $project_name"
    echo "  Project type: $project_type"
    echo "  Engine: Codex CLI"
    echo "  Task source: $task_source_type"
    echo ""

    # Phase 4: File Generation
    echo "━━━ Phase 4/5: File Generation ━━━"

    # Create .ralph/ directory structure
    mkdir -p .ralph/{specs/stdlib,examples,logs,docs/generated}
    echo "  ✅ Created .ralph/ directory structure"

    # Generate PROMPT.md (skip if exists and not --force)
    if [[ ! -f ".ralph/PROMPT.md" ]] || [[ "$FORCE_OVERWRITE" == "true" ]]; then
        if [[ -f "$RALPH_HOME/templates/PROMPT.md" ]]; then
            cp "$RALPH_HOME/templates/PROMPT.md" .ralph/PROMPT.md
        elif [[ -f "$RALPH_ROOT/templates/PROMPT.md" ]]; then
            cp "$RALPH_ROOT/templates/PROMPT.md" .ralph/PROMPT.md
        else
            cat > .ralph/PROMPT.md << 'PROMPTEOF'
# Project Development Instructions

## Overview
[Describe your project goals here]

## Key Requirements
- [ ] Requirement 1
- [ ] Requirement 2

## Technical Guidelines
- Follow existing code conventions
- Write tests for new functionality
- Document public APIs
PROMPTEOF
        fi
        echo "  ✅ Generated .ralph/PROMPT.md"
    else
        echo "  ⏭️  Preserved existing .ralph/PROMPT.md"
    fi

    # Generate fix_plan.md
    # IMPORTANT: Always preserve existing fix_plan.md (contains work progress)
    # Only write if file doesn't exist, or if explicitly importing new tasks with --force
    if [[ -f ".ralph/fix_plan.md" ]]; then
        if [[ "$FORCE_OVERWRITE" == "true" && "$task_source_type" != "local" && "$SKIP_TASKS" != "true" ]]; then
            # --force with explicit task import: overwrite with new tasks
            if type import_tasks_from_source &>/dev/null 2>&1; then
                import_tasks_from_source "$task_source_type" "$GITHUB_LABEL" "$PRD_FILE" > .ralph/fix_plan.md 2>/dev/null || {
                    echo "  ⚠️  Task import failed, preserving existing fix_plan.md"
                }
            fi
            echo "  ✅ Imported new tasks into .ralph/fix_plan.md"
        else
            echo "  ⏭️  Preserved existing .ralph/fix_plan.md (contains work progress)"
        fi
    else
        # fix_plan.md doesn't exist — create it
        if [[ "$task_source_type" == "local" || "$SKIP_TASKS" == "true" ]]; then
            if [[ -f "$RALPH_HOME/templates/fix_plan.md" ]]; then
                cp "$RALPH_HOME/templates/fix_plan.md" .ralph/fix_plan.md
            elif [[ -f "$RALPH_ROOT/templates/fix_plan.md" ]]; then
                cp "$RALPH_ROOT/templates/fix_plan.md" .ralph/fix_plan.md
            else
                cat > .ralph/fix_plan.md << 'FIXPLANEOF'
# Fix Plan

## Priority Tasks
- [ ] Review project structure
- [ ] Set up development environment
- [ ] Implement core features
- [ ] Write tests
- [ ] Documentation
FIXPLANEOF
            fi
        else
            # Try to import tasks from source using shared library
            if type import_tasks_from_source &>/dev/null 2>&1; then
                import_tasks_from_source "$task_source_type" "$GITHUB_LABEL" "$PRD_FILE" > .ralph/fix_plan.md 2>/dev/null || {
                    echo "  ⚠️  Task import failed, using defaults"
                    echo "# Fix Plan" > .ralph/fix_plan.md
                    echo "" >> .ralph/fix_plan.md
                    echo "- [ ] Import tasks manually" >> .ralph/fix_plan.md
                }
            else
                echo "# Fix Plan" > .ralph/fix_plan.md
                echo "" >> .ralph/fix_plan.md
                echo "- [ ] Import tasks from $task_source_type" >> .ralph/fix_plan.md
            fi
        fi
        echo "  ✅ Generated .ralph/fix_plan.md"
    fi

    # Generate AGENT.md (skip if exists and not --force)
    if [[ ! -f ".ralph/AGENT.md" ]] || [[ "$FORCE_OVERWRITE" == "true" ]]; then
        if [[ -f "$RALPH_HOME/templates/AGENT.md" ]]; then
            cp "$RALPH_HOME/templates/AGENT.md" .ralph/AGENT.md
        elif [[ -f "$RALPH_ROOT/templates/AGENT.md" ]]; then
            cp "$RALPH_ROOT/templates/AGENT.md" .ralph/AGENT.md
        else
            cat > .ralph/AGENT.md << 'AGENTEOF'
# Agent Instructions

## Build Commands
- Install: [your install command]
- Build: [your build command]
- Test: [your test command]

## Code Style
- Follow existing conventions
- Use consistent formatting
AGENTEOF
        fi
        echo "  ✅ Generated .ralph/AGENT.md"
    else
        echo "  ⏭️  Preserved existing .ralph/AGENT.md"
    fi

    # Generate .ralphrc with Codex-specific settings
    generate_codex_ralphrc "$project_name" "$project_type" "$task_source_type" > .ralphrc.codex
    echo "  ✅ Generated .ralphrc.codex (Codex CLI)"

    echo ""

    # Phase 5: Verification
    echo "━━━ Phase 5/5: Verification ━━━"
    local all_ok=true

    for file in .ralph/PROMPT.md .ralph/fix_plan.md .ralph/AGENT.md .ralphrc.codex; do
        if [[ -f "$file" ]]; then
            echo "  ✅ $file"
        else
            echo "  ❌ $file (missing)"
            all_ok=false
        fi
    done

    for dir in .ralph/specs .ralph/logs .ralph/docs/generated; do
        if [[ -d "$dir" ]]; then
            echo "  ✅ $dir/"
        else
            echo "  ❌ $dir/ (missing)"
            all_ok=false
        fi
    done

    echo ""

    if [[ "$all_ok" == "true" ]]; then
        echo "╔════════════════════════════════════════════════╗"
        echo "║   ✅ Ralph+Codex enabled successfully!        ║"
        echo "╚════════════════════════════════════════════════╝"
        echo ""
        echo "Next steps:"
        echo "  1. Review and customize .ralph/PROMPT.md"
        echo "  2. Adjust .ralph/fix_plan.md with your tasks"
        echo "  3. Run: ralph-codex --monitor"
        echo ""
    else
        echo "⚠️  Some files could not be created. Check the errors above."
    fi
}

# =============================================================================
# CLI ARGUMENT PARSING
# =============================================================================

while [[ $# -gt 0 ]]; do
    case $1 in
        -h|--help)
            show_help
            exit 0
            ;;
        -v|--version)
            echo "ralph-codex-enable version $VERSION"
            exit 0
            ;;
        --from)
            TASK_SOURCE="$2"
            shift 2
            ;;
        --prd)
            PRD_FILE="$2"
            shift 2
            ;;
        --label)
            GITHUB_LABEL="$2"
            shift 2
            ;;
        --force)
            FORCE_OVERWRITE=true
            shift
            ;;
        --skip-tasks)
            SKIP_TASKS=true
            shift
            ;;
        --non-interactive)
            NON_INTERACTIVE=true
            shift
            ;;
        *)
            echo "Unknown option: $1"
            show_help
            exit 1
            ;;
    esac
done

# Run the wizard
run_wizard
